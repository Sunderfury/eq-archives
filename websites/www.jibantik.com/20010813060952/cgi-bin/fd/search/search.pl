        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>What 
          is an Index?</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Webster's 
          dictionary describes an "index" as a sequential arrangement of material. 
          Our index is a large, growing, organized collection of Web pages and 
          discussion group pages from around the world. The 'index' becomes larger 
          every day as people send us the addresses for new Web pages. We also 
          have technology that crawls the Web looking for links to new pages. 
          When you use our search service, you search the entire collection using 
          keywords or phrases.</font></P>
        <font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><A NAME="2"></A> 
        </font> 
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>What 
          is a Word?</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">When 
          searching, think of a word as a combination of letters and numbers. 
          The search service needs to know how to separate words and numbers to 
          find exactly what you want on the Internet. You can separate words using 
          white space and tabs.</font></P>
        <font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><A NAME="3"></A> 
        </font> 
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>What 
          is a Phrase?</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">You 
          can link words and numbers together into phrases if you want specific 
          words or numbers to appear together in your result pages. If you want 
          to find an exact phrase, use "double quotation marks" around the phrase 
          when you enter words in the search box.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Example 
          #1: To find lyrics by the King, type "you ain't nothing but a hound 
          dog" in the search box. You can also create phrases using punctuation 
          or special characters such as dashes, underscore lines, commas, slashes, 
          or dots.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Example 
          #2: Try searching for 1-800-999-9999 instead of 1 800 999 9999. The 
          dashes link the numbers together as a phrase.</font></P>
        <font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><A NAME="4"></A> 
        </font> 
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>Simple 
          Tips for More Exact Searches</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Searches 
          are case insensitive. Searching for "Fur" will match the lowercase "fur" 
          and uppercase "FUR".</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">By 
          default, all searches are accent insensitive as well, but administrators 
          can change this setting. Accent sensitivity relates to Latin characters 
          like &#245;.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>Including 
          or excluding words:</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">To 
          make sure that a specific word is always included in your search topic, 
          place the plus (+) symbol before the key word in the search box. To 
          make sure that a specific word is always excluded from your search topic, 
          place a minus (-) sign before the keyword in the search box.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Example: 
          To find recipes for cookies with oatmeal but without raisins, try "recipe 
          cookie +oatmeal -raisin".</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>Expand 
          your search using wildcards (*):</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">By 
          typing an * at the end of a keyword, you can search for the word with 
          multiple endings.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">Example: 
          Try wish*, to find wish, wishes, wishful, wishbone, and wishy-washy.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>Searching 
          for web addresses:</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">If 
          your search term is a URL, like "http://www.yahoo.com/", some search 
          engines will redirect you directly to the URL. To avoid this behavior, 
          and do an actual search with the URL as the search term, enclose the 
          URL in double-quotes.</font></P>
        <font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><A NAME="5"></A> 
        </font> 
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>Fancy 
          Features for Typical Searches</B></font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF">You 
          can search more than just text. Here are all of the other ways you can 
          search on the net:</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>link:address</B><BR>
          Finds pages that link to the specified address, or a substring of it. 
          Use link:microsoft.com to find all pages linking to Microsoft sites. 
          Note: this feature is not implemented on all search engines.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>text:text</B><BR>
          Finds pages that contain the specified text in any part of the page 
          other than an image tag, link, or URL. The search text:cow9 would find 
          all pages with the term cow9 in them.</font></P>
        <P><font face="Arial, Helvetica, sans-serif" size="2" color="#FFFFFF"><B>title:text</B><BR>
