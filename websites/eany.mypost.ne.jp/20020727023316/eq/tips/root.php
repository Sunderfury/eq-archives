	<DIV style="margin:30px 0 5px 0; width:400px; border-color:#FAB6CA" class="title1">
		<DIV class="title2" style="border-color:#FAB6CA">使い分けの実際</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box3">
				<DIV>
					の前に、一つだけ。<BR>RootのBreak判定というものが存在します。<BR>これは一定時間置きにRandomで判定され、時間が経つにつれてRootが剥がれる確率は上がっていきます。<BR>また、RootしているMobにDMGを与えた場合も、判定が行われます。<BR>この際に、MeleeによるDMGよりもDDによるDMGを与えたときの方が、剥がれる確率は上がっているように思います。<BR>DMGの量や種類によって、確率が変動しているようです。<BR><BR>
					さて実際にRootを使用する状況というのは、どんなときでしょうか。主に考えられるのは<BR>1) Root→DDによるSolo Play<BR>2) Heal Taunt時に張り付いたMobから逃げる<BR>3) ENCなしでの複数INC時、離れたところにRootで放置<BR>4) RNGなどの打たれ弱いAttackerが、Tauntしないように距離を微調整<BR>5) ENCが寝かしたMobを起こすときに、ENCに張り付かないようRootしてからAttack<BR>といったところでしょうか。<BR>順番に検証していきましょう。<BR><BR>
					※今後、足止めすることをRoot、Spell:RootとSpell:ImmoをRoot系、<BR>※Spell:EnstillとSpell:P-EarthをEnstill系と呼称します。<BR><BR>
					<SPAN style="font-weight:bold">☆ 1)のPattern</SPAN>
						<BLOCKQUOTE>
							DDによりRootが剥がれる確率は、経験的に割と高いです。<BR>Rootが剥がれれば当然張り付かれるので、殴られながらCastすることになります。<BR>もちろん、十分に距離を取っておけば、殴られる前にもう一度Rootできるかも知れませんが、<BR>CTが長いと殆どの場合は間に合わないでしょう。<BR><BR>
							せっかく多くのManaと時間を使って長い効果時間を期待しても、<BR>DDですぐ剥がれてしまうのでは意味がありません。<BR>というよりも、むしろ無駄です。<BR><BR>
							ですのでこの場合、<SPAN class="em">Root系を使うのがBest</SPAN>でしょう。<BR><BR>
						</BLOCKQUOTE>
					<SPAN style="font-weight:bold">☆ 2)のPattern</SPAN>
						<BLOCKQUOTE>
							CLRにとって一番恐ろしいのがHeal Taunt。<BR>いくらACやHPをあげても、防御Skillに乏しいCLRはやはり柔らかいです。<BR><BR>
							殴られながらCastすることになるのは1のPatternと同様ですので、<BR>やはり<SPAN class="em">Root系を使うのがBest</SPAN>でしょう。<BR><BR>
						</BLOCKQUOTE>
					<SPAN style="font-weight:bold">☆ 3)のPattern</SPAN>
						<BLOCKQUOTE>
							俗に言うParkingです。<BR>PartyにPALがいる場合はお任せした方がいいのですが、必ずそういうPartyとは限りません。<BR>ほかのPlayerがMobを引きつけている間に、CLRが足止めすることも戦術としてあり得ます。<BR><BR>
							こういう場合には、なるべく長い間離れていて欲しいですから、<BR>当然Rootは<SPAN class="em">Enstill系を使う</SPAN>ほうがいいですね。<BR><BR>
						</BLOCKQUOTE>
					<SPAN style="font-weight:bold">☆ 4)のPattern</SPAN>
						<BLOCKQUOTE>
							これは「RootされたMobはHate Listに関係なく、<BR>一番近くにいるPCをTargetにする」習性を利用した戦術です。<BR>言うまでもなく、EQ中もっとも信頼の置ける壁はWARです。<BR>他Classを凌駕するHP量と、高い防御Skillを持つからです。<BR>故にCLRとしては、他の人には殴られて欲しくありません。<BR>なぜなら最もMana効率のいいHealがComplete Healing(CH)であり、<BR>WAR以外では安心してCHを使えないからです。<BR><BR>
							戦闘中にMobをRootし、WARは極端にMobに近寄って殴ります。<BR>他のAttackerは距離を取って殴ることにより、MobのTargetはWARに安定するわけです。<BR><BR>
							PCがMobにDMGを与えるごとにRootが剥がれるかどうかの判定をするわけですので、<BR>大抵の場合Rootはすぐ剥がれます。<BR>よって<SPAN class="em">Root系を使うのがBest</SPAN>です。<BR>何度もかけ直しながら戦闘することになりますが、MobのHPが減ってきたら逃走防止に<BR>Snare・Darknessをかけますので（もちろん使用できるClassがPartyにいればですが）、<BR>Root効果が切れたことをMemberに報告する癖を付けましょう。<BR>Rootがかかった状態ではSnareはHoldしません。<BR><BR>
						</BLOCKQUOTE>
					<SPAN style="font-weight:bold">☆ 5)のPattern</SPAN>
						<BLOCKQUOTE>
							ENCのPartyにおいて複数MobがINCした場合、ENCがMain Target以外を寝かせます。<BR>Main Targetを始末したら次は寝ているMobをMain Targetにするわけですが、<BR>この時にすぐMelee攻撃を開始すると十中八九ENCに飛んでいきます。<BR>ENCはCLRと並んでPartyの生命線ですので、このような事態を避けるために、<BR>攻撃を開始する前にまずRootが一般的です。<BR><BR>
							最初だけRootが利いてくれればそれでいいため、<SPAN class="em">Root系を使うのがBest</SPAN>です。<BR><BR>
						</BLOCKQUOTE>
