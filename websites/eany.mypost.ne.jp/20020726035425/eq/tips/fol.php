	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box2">
				<DIV>
					まずは消費Mana・Cast Timeなどについて。<BR>FoLはCLR・SHMのRank1Spellです。よって消費Manaも12と少なく、CTも1.5と早いです。<BR>ついでにリキャストに要する時間も短いので連射することが可能となっています。<BR><BR>
					どんな効果があるかというと・・・試しに自分をTargetして撃ってみましょう。<BR>画面が真っ暗になりますね。Chatを読むこともできません。<BR>でもKeyboardは生きてるので、「makkura-」とか伝えることは可能です。<BR>ついでにHot Keyに登録してあるSpellもCast可能です。<BR><BR>
					では一体どんな意味があるんでしょうか。<BR>一つはPvP戦。これは結構効きます。<BR>なんせ真っ暗ですから、その隙に好きなことができます。<BR>でも普通ServerのPlayerは、PvPで有利なSpellを持っていてもなんの得にもなりません。<BR>Duelで使っておもしろいぐらいですかｗ<BR><BR>
					FoLの性質を列挙してみましょう。<BR>・相手の視界を奪う<BR>・Tauntの補助として使える<BR>・<SPAN class="em">NPCに使用すると、その攻撃範囲にPCがいるかどうかで行動が変化する</SPAN><BR>この3つ目が大事な点であるのです。もう少し詳しく説明しますと・・・<BR>Mobと戦闘中、FoLを入れてみます。しかし何事もなかったかのようにMobは殴ってきます。<BR>しかし実際にはMobは目が見えなくなっているのです。<BR>試しにMobから数歩下がって、攻撃範囲からはずれてみてください。<BR>MobはPCを追いかけようとするのですが、目が見えないのであさっての方向に走っていきます。<BR>これってつまり、Fearと同じことですね？
				</DIV>
