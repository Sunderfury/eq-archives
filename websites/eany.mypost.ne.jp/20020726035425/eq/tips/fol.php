		<DIV class="title2">FoL Fearの使い道</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box2">
				<DIV>
					TankがいないPartyで、かつ広い場所で狩りをするときに有効です。<BR>例えばRathe山でHGやCycを狩るとき。<BR>FoLかけて数歩Back、逃げてる隙にNuke連射でかなり楽勝で倒せると思います。<BR>Karana全般のMobやTimoのSpiloc、FeerrottのはぐれSpec。<BR>使える場所はいろいろあると思いますが、やはりフィールド専用でしょうか。<BR>他にも、危ないときにFoL→逃げというのもありですか。<BR><BR>
					どちらにせよLvl40を越えてしまうと、もう使い道はないかも知れません。<BR>TimoのSpirocをWIZと二人で・・・ってのは50越えてからもやりましたけどｗ<BR><BR>
					普通のFearは40ManaでCT3、Invoke Fearは100ManaでCT2.5です。<BR>しかもどちらも、リキャストに5秒程度の時間がかかってしまいます。<BR>そう考えると単にFear的効果を得るためだけなら、FoLは非常にManaコストに優れ、<BR>Resistされてもすぐにリキャストできるために優秀であるといえるのではないでしょうか。<BR>ちなみに効果時間はFoLが約12秒、上位版のBlinding Luminanceは倍の24秒となっています。<BR>ただUndeadについては専用Fearの性能がいいので、そちらを使った方がいいでしょう。
