		<DIV class="title2" style="border-color:#FAB6CA">Archtype Abilities(Healer)</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box3">
				<DIV>
					Lv55以上かつGeneral Abilitiesに6ポイント以上消費したキャラクターが使用できるスキルです。ArchtypeはWAR/ROG/MNKのMelee・ENC/MAG/NEC/WIZのCaster・CLR/DRU/SHMのHealerと3種類に分かれています。なお、PAL/RNG/BSTはMeleeとHealerから、BRD/SHDはMeleeとCasterからそれぞれ選択可能です。
					ここではClericの属するHealer Archtypeのみ列挙します。なお、スキルレベルを上げるためには[スキルレベル x 2]ポイント必要となりますので、スキルレベル3まで上げるには12ポイントを要します。<BR><BR>
					<TABLE border=1 cellpadding=5>
						<TR>
							<TH width=20%>名称</TH>
							<TH width=5%>上限値</TH>
							<TH width=75%>説明</TH>
						</TR>
						<TR>
							<TD>Channeling Focus</TD>
							<TD align="center">3</TD>
							<TD>スキルレベルによってSpell詠唱が中断される(Interrupt)確率を減らします。これはMOBに殴られながらでも、Castできる可能性が増えると言うことです。(5%/10%/15%)</TD>
						</TR>
						<TR>
							<TD>Healing Adept</TD>
							<TD align="center">3</TD>
							<TD>スキルレベルによってCelestial HealingなどのHoT(Healing over Time)以外のHeal量を増加させます。(2%/5%/10%)</TD>
						</TR>
						<TR>
							<TD>Healing Gift</TD>
							<TD align="center">3</TD>
							<TD>Celestial HealingなどのHoT(Healing over Time)以外のHealにおいてCritical Heal(2.1倍)を発生、スキルレベルによって確率が上昇します。(3%/6%/10%)</TD>
						</TR>
						<TR>
							<TD>Mental Clarity</TD>
							<TD align="center">3</TD>
							<TD>スキルレベルによってMana Regenerationが1point/tick付きます。</TD>
						</TR>
						<TR>
							<TD>Spell Casting Fury</TD>
							<TD align="center">3</TD>
							<TD>DD(Diret Damage)SpellにCritialを発生、スキルレベルによって確率が上昇します。(2%/4%/7%)</TD>
						</TR>
						<TR>
							<TD>Spell Casting Mastery</TD>
							<TD align="center">3</TD>
							<TD>スキルレベルによってSpecializationスキルの発動率を上昇させます。つまり、全てのSpellにおいてFizzle率が低下し、Mana Costが減少する確率が増えます。(5%/15%/30%)</TD>
						</TR>
						<TR>
							<TD>Spell Casting Reinforcement</TD>
							<TD align="center">3</TD>
							<TD>スキルレベルによりBuffの効果時間が延長します。(5%/15%/30%)</TD>
						</TR>
					</TABLE>
					<BR><BR>
					どのスキルを選ぶか、Clericにとって一番悩ましいところではないでしょうか。Healing Gift/Mental Clarity/Casting Masteryの3つは特に有効だと思われます。Casting FuryはClericには不要なのでまっさきに除外。
					Channeling Focusは、Raid中心のプレイの場合、MOBのMelee DMG増加に伴ってあまり効果はないように思います。ReinforcementはあくまでLv60 CLRの視点で言えば必要ないでしょう。150minも効果の続くAegolismしかBuffを使用しないため、
					頻繁にSpellを入れ替えたりすることがないからです。Giftは運次第なために優先順位がやや下がるように思いますが、緊急回避時以外はCelestial ElixcerかCHがメインであることを考えれば、それなりに有効であるといえるでしょう。
					個人的にはMasteryとClarityでかなり悩みます。
				</DIV>
