			<TD class="box3">
				<DIV>
					拡張パックShadows of Luclinを導入することによって、Lv51以上のキャラクターでスキルシステムが機能するようになります。MOBを倒した際に得られるEXPを、レベルアップ用とスキルアップ用に任意に振り分けることが可能です。死亡時のペナルティはレベルアップ用のEXPからのみ差し引かれ、スキルアップ用のEXPは減ることはありません。
					従来までのEXPバーの下にスキルEXPバーが追加されており、黄色い目盛りが振り切るとスキルポイントに1加算されます。ポイントは好きなスキルに割り振ることができますが、スキルは3段階になっており最初はGeneral Abilities（全Class共通スキル群）からしか選べません。次の段階であるArchtype Abilities（Class種類別スキル群...Melee/Caster/Healerの3種）を習得するにはLv55以上でかつGeneral Abilitiesに6ポイント以上振っておかなくてはなりません。
					最終段階であるClass Abilities（Class別スキル群）を習得するにはLv59以上かつArchtype Abilitiesに12ポイント以上振っておく必要があります。<BR><BR>
					一定のスキルポイントを消費することにより、称号を得ることができます。例えばGeneral Abilitiesに6ポイント振ると、キャラクター名の前に男性はBaron、女性はBaronessという称号がつきます。Clericの場合、消費したポイントによりさらにVenerable・Exarchとなります。
