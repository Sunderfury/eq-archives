		<DIV class="title2">Class Abilities(Cleric)</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box2">
				<DIV>
					Lv59以上かつArchtypeに12ポイント以上消費したキャラクターが使用できるスキルです。Class Abilitiesには2種類あり、1つはArchtype同様3段階まで上昇させることのできるもの。スキルレベルの上昇には[スキルレベル x 3]ポイント必要となりますので、スキルレベル3まで上げるためには18ポイント要します。
					もう1つは1段階しかないスキルで、これを使用可能にするためにはスキルによってそれぞれことなったスキルポイントが必要になります。また、再使用可能時間の表記されていないスキルはパッシブ型です。
					<TABLE border=1 cellpadding=5>
						<TR>
							<TH width=20%>名称</TH>
							<TH width=5%>上限値</TH>
							<TH width=75%>説明</TH>
						</TR>
						<TR>
							<TD>Bestow Divine Aura</TD>
							<TD align="center">1</TD>
							<TD>6スキルポイント必要。SpellのMemorizeやMana消費なしに（つまりPALのLHやSHDのHTのようなスキル)他人にDivine Auraをかけることが可能になる。144minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Celestial Regeneration</TD>
							<TD align="center">1</TD>
							<TD>5スキルポイントとHealing Gift Lv3が必要。SpellのMemorizeやMana消費なしにSingle Targetに対して強力なHoTをかけます。144minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Divine Resurrection</TD>
							<TD align="center">1</TD>
							<TD>5スキルポイントとHealing Gift Lv3が必要。SpellのMemorizeやMana消費なしに100% EXP Backの蘇生を行います。4320minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Innate Invisibility to Undead</TD>
							<TD align="center">1</TD>
							<TD>3スキルポイント必要。SpellのMemorizeやMana消費なしにInvisi vs Undeadが使えます。7minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Mass Group Buff</TD>
							<TD align="center">1</TD>
							<TD>9スキルポイント必要。次にCastするGroup BuffのMana Costが2倍になるかわりに、AEとして効果が出ます。72minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Purify Soul</TD>
							<TD align="center">1</TD>
							<TD>5スキルポイントとHealing Gift Lv3が必要。SpellのMemorizeやMana消費なしにCharm/Fear/Res Effectを除くすべての負の属性を対象から取り除きます。72minで再使用可能。</TD>
						</TR>
						<TR>
							<TD>Spell Casting Reinforcement Mastery</TD>
							<TD align="center">1</TD>
							<TD>8スキルポイントとSpell Casting Reinforcement Lv3が必要。Buffの効果時間が50%延長されます。</TD>
						</TR>
						<TR>
							<TD>Turn Undead</TD>
							<TD align="center">3</TD>
							<TD>SpellのMemorizeやMana消費なしに、Undeadに対するAE Fear+DoTを放ちます。スキルレベルによって効果が上昇します。72minで再使用可能。</TD>
						</TR>
					</TABLE>
					<BR><BR>
					ここにたどり着くまでが大変な割には、それほど効果的なスキルが揃ってないなという印象。特にDivine Resurrectionの再使用可能まで4320min（72hours = ゲーム時間で60days）はあんまりにも酷すぎ。
					いったい何人のClericがそんなスキルにポイントを使うというのでしょうかね。まだ詳細が不明なのではっきりとしたことはいえませんが、ぱっと見た感じ使えそうなのはCelestial Regenerationくらいしかないように思えます。
					Bestow DAはMain TaunterにかけてしまうとMOBがどこに跳ねてしまうかわからないので、使い道に悩むところです。Pullerにかけるぐらいでしょうが、「それがあればPull楽勝〜」っていう場所があるのかどうか疑問です。
					Invisi to Undead・Turn Undeadは本気でどうでもいいですし、Reinforcement MasteryもArchtypeのほうと同様の理由で必要ないでしょう。Mass Group BuffはGroup Aegolismで使うと便利かなと思ったりもしたのですが、
					よく考えてみたらAegoはPotGを上書きしてしまうので、Casterが避難するなりしないといけないのが面倒です。またManaが2倍になるため4000manaも使ってしまいます。これは殆ど全てのManaを使い切ってしまうようなものなので、
					Raid時の全体のBuff効率から見て、あまりよろしくないように感じます。1人がOoMになるより2人で半分ずつのほうが回復早いですから。Purify Soulは使えるのかどうなのか、正直現状ではよくわかりません。The Deepに
					Fizzle率200%アップとかいう技を使ってくるMOBがいるそうで、それ用なのかと思ったりしますが...単体にしか使えないので自分用？しかも1回コッキリですし。また、SpellにGreater Remove Curseというものがあるのですが、
					それも同じような効果なのかどうかも不明。Spellで代用が利くなら、このスキルダメダメですが。というわけでCelestial Regenerationに期待するわけですが、一体何秒でどのくらいの回復量なのか、まったくわからないので
					これもなんとも言えません。SpellにHealing Completeという10k HoTがあるようですので、それより効果が高いのだろうと予想はつきますが。
				</DIV>
			</TD>
