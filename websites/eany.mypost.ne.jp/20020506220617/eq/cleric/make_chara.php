			<TD class="box2">
				<DIV>
					Clericになることが出来る種族はHalfElf、WoodElf、Barbarian、Ogre、Troll以外です。私はこのゲームを買う以前から、「やるならHighElfの僧侶でやりたいな」と決めておりました。色々なファンタジー小説やゲームで、愛着のある種族だったからです。<BR><BR>
					このゲームにおけるHighElfの特徴は、<SPAN class="em">低いSTR&STA、高いWIS&INT</SPAN>です。HighElfが選択できるClassはClericの他、Enchanter、Magician、Paladin、Wizardがありますが、Clericをやるのなら申し分ない種族選択であるといえます。その反面、HighElfのClericは溢れていますので、個性を主張したい人には向かないかも知れません。<BR><BR>
					ClericはHighElf以外でやったことがないので何とも言えませんが、後述するポイントの振り方に関わらず装備で困らないのは、DwarfとHumanではないかと思います。特にDwarfはClericとしてはずば抜けたSTRとSTA、多種族よりも5高いsvMagic、そしてそこそこにあるWIS。ただCHAの低さも考慮しなければ、後悔するかも知れません。<BR><BR>
					Humanは平均的なステータスを持ち、殆どすべてのクラスになることができます。感情移入もしやすいと思われますが、暗視能力を持たないのが泣き所です。EruditeもHuman系ですがこちらは高いWIS&INTを持っています。ただしHuman同様暗視能力を持っていません。その代わり、HumanとEruditeには専用Questが用意されており、なかなか性能のよい種族限定Itemを装備することが出来ます。また暗視については、LevelがあがればMagicianが召喚してくれるItemで目をよくしたり、暗視能力のつく装備を入手したりすることで克服できます。<BR><BR>
					GnomeはClericとしてはやや厳しいかも知れません。WIS初期値77ですので、ポイントはすべてWISに振ることになるでしょう。私はGnomeのClericを殆ど見たことがありません(単に気付いてないだけかも知れませんが)。個性派向けです。<BR><BR>
					DarkElfは上級者向けの種族です。Factionを上げなければ殆どの街に入ることが出来ず、序盤はかなり辛いと思われます。とことんやりこむ気がある人、とにかくDarkElfに惚れた人や、2nd向けでしょう。辛い代わりに、Ultra Visionという強力な暗視能力を持っています。<BR><BR>
					ClericはPlateまで装備可能なClassであるので、Leather・Chain・Plateと幅広く選べます。種族・性別によって見た目がそれぞれ異なるので、それも種族選択する際に考えに入れておいてもいいでしょう。見た目重視で選択しようが、Clericとしての性能重視で選択しようが自由です。一番後悔しない選び方は、一番気に入った種族を選ぶことじゃないかと思います。
