	<DIV style="margin:30px 0 5px 0; width:400px; border-color:#FAB6CA" class="title1">
		<DIV class="title2" style="border-color:#FAB6CA">BonusPointの配分</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box3">
				<DIV>
					私はろくに調べもしないまま、HighElfのClericでキャラを作成し、30ポイント与えられるボーナスポイントを、かなり適当に振ってしまいました。後悔したこともありますが、最終的には特に気にするようなことではありません。<SPAN class="em">ClericのManaの総量は、LevelとWISによって決定</SPAN>されます。従ってもっとも賢いボーナスポイントの振り方は、<SPAN class="em">WISに大量に注ぎ込む</SPAN>ということになるでしょう。<BR><BR>
					ここで慌ててポイントを全部（といっても30のうち25までしか割り振れません）WISに振る前に、ちょっと考えてみてください。もしあなたが完全にWIS重視で、重たい装備は着ける気がないというのであれば問題ありません。しかし「やっぱりPlateも着てみたいよ〜」という人は、STRにもポイントを振っておく必要が生じます。STRが低いとそれだけ装備可能な重量に制限が出来てしまいます。正確に言えば、どんなにSTRが低くても重い装備をすることは出来ますが、装備重量+持ち物重量がSTRの値を越えると、その分だけ動きが遅くなってしまうのです。戦場において敗走を余儀なくされた場合、<SPAN class="em">動きの遅さは死に直結</SPAN>します。ある程度Levelが上がって、軽くて性能の良いMagicItemを取ることが出来るようになるまでは、STRが足かせになることがままあります。Level20前後までを楽にしたければ、STRにもポイントを振りましょう。後半を楽にしたいのであれば、STRは無視してすべてWISに振りましょう。この辺はもう、好みの問題としか言いようがありませんが・・・。またLevelがあがればParty MemberがSoWを使えるようになったり、逃げるときもGGを使ったりするようになるので、多少の重量Overは問題にならなくなります。ただあまりにもOverするとAGIが下がり、同時にACも大量に下がります。HighElfやDarkElfはSTR不足になりがちなので、その辺を考慮に入れておくといいかもしれません。また、HPに直結しているステータスであるSTAも無視できません。Clericの死はParty全滅の危機です。よって万が一のときに、一撃でも多くの攻撃に耐えられるHPも必要となってきます。Dwarfなどは元からSTRやSTAが高いので問題ないと思いますが、貧弱なClassだとSTAも欲しくなるでしょう。<BR><BR>
					結局のところ「これがBest」といえる配分はないでしょう。初期ステータスで足りない部分は、後々装備で補えるのですから。WISを軸に、選択した種族によって低い部分に配分しておけば、とりあえずいいんじゃないでしょうか。
