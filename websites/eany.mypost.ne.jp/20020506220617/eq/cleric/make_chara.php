					Clericとは、所謂僧侶です。自らの仕える神の下僕として、Heal(回復)やResurrecttion(蘇生)に代表される神の奇跡を、自分自身の体を触媒として代行する者(だと思っています)。しかし別に「一日一回は祈りを捧げなくてはならない」とか「寺院に寄付を納めなければならない」などといった制約は一切ありません。ですから聖人を目指して他人を癒すことだけに専念するのも、目の前でHelpと言っている人を見捨てるのも、せこく金をためてウハウハになるのも、全く個人の自由であります。<BR><BR>
					種族選択とボーナスポイントの配分について書いていきますが、一つだけ極めて重要な注意点があります。それは、どの種族(Class)を選んでも、どのようにポイントを振ろうとも、<SPAN class="em">「すべてにおいて完璧」ということはありえない</SPAN>、ということです。どうやっても一長一短があり、お互いの短所を補い合うパーティープレイを楽しむのがこのゲームだと思っています。ですからこんな所を見ないで、好きなように自分の分身を作るというのもまた楽しいのです。
