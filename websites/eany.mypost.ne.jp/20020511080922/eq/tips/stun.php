	<DIV style="margin:30px 0 5px 0; width:400px;" class="title1">
		<DIV class="title2">上位Stunをどう使うか</DIV>
	</DIV>
  <TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box2">
				<DIV>
					こちらはDMGを与えるStunのために、ENCとのSleepの相性はよくありません。<BR>逆に言えば、Manaに余裕があればStunをとりあえず撃つ場面で<BR>DMG＋Stunを期待して上位魔法を撃っておくことは可能です。<BR>また、Mobが逃げかけたときで特に慌てて動きを止めなくてもいいとき（すぐにはLinkしない場所）、<BR>同じくDMG＋Stunを期待して撃っておけますね。<BR>とどめを刺すのに殴りのみじゃ時間がかかる、かと言ってDDじゃOver Killというときにも便利です。<BR>DRU Ringで座って雑談中、熊に襲われたときもSound of Forceで殺せますｗ<BR><BR>
					またStun系Spellはリキャストが長いため、1つのStunでは連射不可能です。<BR>いやらしいSpellを使ってくるMob（例えばComplete Healing）には複数StunをSETして、Stunハメ。<BR>Stun2つだと少し間を開けて、3つだと順番にNon Stop Stunが実現できます。<BR><BR>
					また、RaidなどでDMG Stunが有効な場合も実はあります。<BR>DMGのある上位Stunは、<SPAN class="em">着弾時にStunを撃ったPCが向いている方向にKnock Backする</SPAN>のです。<BR>これを利用して、Mobが壁に埋まって自殺したり（死体も消えてEXPもなし）、<BR>変な場所にWarpしてSummonされたり、Addしたりする危険を減らすことができます。<BR>後ろ向きになって撃てばMobはややこちらに寄ります。<BR>位置を調整するのはTankの仕事ですが、CLRでもお手伝いできるわけです。<BR>これはDMGが大きいほど、その移動距離も大きくなります。<BR>体感的に、Holy Might程度ではそれほど動きませんが、<BR>Sound of ForceやEnforced Reverenceだと相当動きます。<BR><BR>
