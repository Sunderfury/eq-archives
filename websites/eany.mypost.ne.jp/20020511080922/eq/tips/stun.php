	<DIV style="margin:30px 0 5px 0; width:400px; border-color:#FAB6CA" class="title1">
		<DIV class="title2" style="border-color:#FAB6CA">下位Stunの特徴</DIV>
	</DIV>
	<TABLE width="600" border="0" cellspacing="0" cellpadding="0">
		<TR>
			<TD class="box3">
				<DIV>
					他のStunとの違いは<BR>1) CTが早い<BR>2) Mana消費が少ない<BR>3) <SPAN class="em">DMGを与えない</SPAN><BR>とくに3が重要です。<BR><BR>
					CTが早いというのは、どんなSpellにせよいいことです。<BR>Stunはただ相手の動きを一定時間封じる、というだけのSpellではなく、<BR><SPAN class="em">相手のSpell Castを中断する</SPAN>という大事な役割があるのです。<BR>Lvlがあがっていくに従って、強力なDDを使ってきたり、HealをしたりするMobが増えていきます。<BR>それをまともに使わせていると、かなり大変です。<BR>そこで、MobがCastを始めたらすかさずStun。<BR>これで、Resistされなければ100%Castを中断させることができます。<BR>ということは、Cast中断目的で使う場合は、CTは早ければ早いほどいいことになりますね。<BR>相手のCastを確認してから、後出しでStunを打っても間に合うわけですから。<BR><BR>
					SnareのないPartyの場合Rootで足止めするわけですが、突然切れて怖い思いをします。<BR>そんなときもCTの早いStun。<BR>そこでStunで固めるか、再度Rootするかは状況次第ではありますが。<BR><BR>
					また消費Manaが少ないために、いくら撃ってもタダ同然です。<BR>たった35Manaで4秒Mobの攻撃が止まるのです。<BR>（資料では4秒となってますが、実質4秒止まってない気もしますがｗ）<BR>LvlがあがるにつれてMobのMeleeが痛くなるので、35Manaで300以上DMGを軽減できることも珍しくありません。<BR>300というとGHの回復量と同じです。その効果が35ManaのStunで実現可能なのです。<BR><BR>
					またCTの早さゆえに、とりあえず撃っておいても次の瞬間Healできる、というのも魅力です。<BR>CLRのDDはCTが長いですので、Cast中に不意のADDでTankのHPがどんどん減って・・・<BR>となると間に合いませんね。<BR>CLRのDDなんか大したものではないので、暇ならStun撃ってる方が安全確実です、やはり。<BR><BR>
					最も大事な3ですが・・・なぜDMGを与えないのが大事なのでしょうか。<BR>Stunのついでに少しでもDMGいけば、そっちが得じゃんと思いますか。<BR>その答えは<SPAN class="em">ENCのSleepとの相性</SPAN>にあるのです。<BR><BR>
					ENCというClassはどうしてもTauntしてしまうキャスターなのです。<BR>他のNukerなどは戦闘の最初はタンクがしっかりTauntするまでCastを押さえたりディスペルすることによって<BR>自分の所に来るのを押さえることができますが、ENCは最初よけいなMobを寝かす必要があります。<BR>そのときSleepがResistされると、ENCは殴られながらStun→SleepのComboで寝かしに行くわけですが<BR>その時CLRからもStunでSleepの成功率を引き上げてあげることが可能なわけです。<BR>無論、場合によってはHeal最優先なのは言うまでもないことです。<BR>で、このケースではStunならSleepが入った後に着弾してもMobを起こすことはありません。<BR><BR>
