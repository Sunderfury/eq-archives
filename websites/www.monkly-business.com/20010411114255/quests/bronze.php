<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Quest Guides</u></b><p><ul></FONT><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a">Shackle of Bronze</FONT></p><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><ul><p>This quest originates in Cabilis, but has more involvement with "an iksar 
master" in the Timorous Deep.</p><p>In order to initiate the quest, go to Master Niska (in Cabilis, located near 
Master Glox) and state that "I will be your personal courier."  She will hand 
you a Tin Box which weighs 8 pounds, and request that you take it to Master 
Rinmark, who in fact identifies as "an iksar master."  Master Rinmark is 
located on the chess-board island in the Timorous Deep.  He stands meditating 
atop a mountain peak on the island (you should have no problem finding him as 
the island is quite small).</p><p>Once the Tin Box is handed in to "an iksar master" (Master Rinmark) the 
following reaction will be triggered:</p><p>"An Iksar master opens the box to reveal a variety of pebbles.  How grand!! 
My pebbles!! Now I can continue with my rock garden!! Would you please 
[assist old Rinmark in completing this rock garden]?"</p><p>You say : "I will assist old Rinmark in completing his rock garden"</p><p>"An iksar master pours the pebbles from the box into his pocket and hands it 
to you.  Take my box, Fill and combine within my box the following: a Radiant 
Medeorite, Frontier Fools Gold, Petrified Redwood, Rile's Sand Coin, Sulfur 
Stone, Fragment of Tektite, Plains Pebble, Jade Magma, Heart of Ice and an 
Unholy Coldstone.  Return the Full Tin Box to me and I shall reward you with 
a Shackle of Bronze."</p><p>The components are easy.  This will probably be one of the most tedious and 
boring quests you will ever do.</p><p><LI><STRONG>Radiant Meteorite</STRONG> - Foraged in the Burning Woods.  Took me about 2 hours.<LI><STRONG>Frontier Fools Gold</STRONG> - There are indeed 2 different forageable gold nuggets.  
Our desired nugget is will identify as Frontier Fools Gold.  The one we DON'T 
want and WILL NOT combine in the box is Genuine Gold Nugget.<LI><STRONG>Petrified Redwood</STRONG> - grr, more foraging... took me a short 10 minutes though.  
head to Warsliks Woods for this.<LI><STRONG>Rile's Sand Coin</STRONG> - Some more foraging, stay in Timorous Deep and kill raptors 
or something, this one took me a long time.<LI><STRONG>Sulfur Stone</STRONG> - These drop from Drolvarg Scavengers.  Scavengers can be found 
roaming the middle of the zone.  Walk straight outwards once you zone in from 
Lake of Ill Omen.  They're really known as "Scarlet Stones," but with the 
identify spell these are indeed Sulfur Stones.<LI><STRONG>Fragment of Tektite</STRONG> - Pick up 2 carnelian gems.  Grab a friend to cast the 
invisibility spell on you.  At the entrance to the evil outpost (coming in 
from overthere) you will see two gargoyles.  Run into the tunnel with 
invisibility and at the other end of the tunnel will appear two more 
gargoyles, with one of having the name "Tektite."  He wants a replacement for 
his eyes, and seeks two Carnelian gems.  Run up behind him, feign, and hit 
sneak until it works.  Get back up (DO NOT HAIL HIM) and hand him the two 
gems in seperate slots.  You will receive a piece of "Tektite."<LI><STRONG>Plains Pebble</STRONG> - Again, more foraging.  head to the karanas for this.  I got 
mine in south karana.  practiced safe fall and bandaging using krak 
windchaser, and the aviak tower.  Good deal I guess.<LI><STRONG>Jade Magma</STRONG> - Kill burynai miners in Frontier Mountains.  My first kill 
yielded a Jade Magma.<LI><STRONG>Heart of Ice</STRONG> - In Lake of Ill Omen, these drop from the undead "Ice bone 
skeletons" near the lake shore.<LI><STRONG>Unholy Coldstone</STRONG> - In permafrost caverns, a goblin priest will drop them.  
Priests are random common spawns deeper inside permafrost.  I got mine in the 
Preacher room.</p><p>This quest is long, tedious, but well worth the reward, a Shackle of Bronze.  
AC and magic resistance in one bracer.  Humans might wonder as to why we go 
through so much trouble.  Well to keep it short you have to be an Iksar to 
know what its like not being able to wear any sort of plate-mail armor.</p></ul></ul></FONT>

