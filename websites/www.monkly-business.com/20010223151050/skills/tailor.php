<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Skill Guides.</u></b></FONT><p><ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><CENTER><STRONG>Sensei's Tailoring Guide</STRONG></CENTER></FONT></p><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a">
<P>This is a monk's guide to tailoring, and assumes you are only doing 
        this with the intention of making raw/cured silk armor, Wu's Fighting 
        Gauntlets and perhaps a 10-slot backpack or two. If you desire to move 
        beyond these (to mastery level, which is basically reinforced), I would 
        recommend checking Alakazam's skill's section for more info on 
        tailoring. Tailoring is a massive pain in the ass. You have to hunt for 
        components for each attempt, it doesn't skill up very fast, and at the 
        high end you will have to not only work 2-3 other trade skills (brewing 
        and smithing) to create pieces that will skill you higher, but for some 
        items you have to outright buy components (i.e., poison vials). Be that 
        as it may, this is a quick and dirty guide to getting you to the level 
        required to make monk items. As with any trade skill, it is important to 
        know what item goes trival at what point. The trivial chart for 
        tailoring is: 
        <P>Silk Swatch: 17<BR>Patchwork: 26<BR>Raw Silk: 39<BR>Studded: 
        57<BR>Cured Silk: 82<BR>Wu's Fighting Gauntlets: 88<BR>Backpacks: 90 
        <P>The first step is to put in as many points as you can into the skill 
        (20). This will simply save you time and hassle in the long run. If you 
        cannot do this, you can work the skill up to 17 by making silk swatches 
        (combine 2 spider silks in your sewing kit). 
        <P>Patchwork is the first armor triv (26), and is created by combining a 
        tailoring pattern with a ruined wolf/bear/lion pelt in a sewing kit. The 
        easiest way to do this is to scour the shops near the newbie hunting 
        areas where these mobs spawn and buy them. If you're the sadistic sort, 
        however, you can hunt them to your heart's content (they don't stack, 
        however, so bring your kit and patterns with you). 
        <P>There are no recipes for Quivers and Fleeting Quivers despite the 
        patterns being available. The book also has no recipe for Reinforced 
        Leather Wristbands but they can be made using 1 steel boning. Studded 
        tunics, Cloak, Shoulder, and Belt are broken. Reinforced Masks are 
        broken. 
        <P>After patchwork, it's time to start with raw silk (trivs at 39). Raw 
        silk armor is made by combining a tailoring pattern with a silk swatch 
        (2 spider silks, as mentioned above). All pieces use 1 silk swatch 
        except pants and tunics (2 each). As all raw silk trivs at the same 
        point, it's obviously wiser to not do pants and tunics to train with. 
        Spiders can be found all over the map, but I've found the heart spiders 
        in Upper Guk to be the easiest place to farm for silk. 
        <P>At skill 39, the fun really begins with this skill. Studded armor 
        requires metal studs, which are made by blacksmiths, triv at 41 
        (smithing) and do not stack. If you don't have a friend in the business, 
        here's a quick and dirty smithing guide to get you to making 'em: 
        <P>
        <OL>
          <LI>Put 20 points in smithing; 
          <LI>Get a ton of backpacks in your inventory; 
          <LI>Make zillions of metal bits. These are made by combining 2 small 
          ores with a flask of water (not summoned water mind you). They stack, 
          and triv at 21. 
          <LI>Make studs. These are made by combining (in a forge) 3 bits, a 
          file and a flask of water (not summoned). 
          <P></P></LI></OL>Studded armor is made with medium pelts (Oasis and 
        South Ro are nice hunting spots for these). As always, check the nearby 
        merchants if you want to skip hunting and just buy them. As all studded 
        trivs at the same point (57), it's easiest to just make studded masks (1 
        stud, 1 mask pattern, 1 pelt combined in a large sewing kit). 
        <P>Assuming you've made it this far, it's time for cured silk. Cured 
        silk armor requires a tailoring pattern, silk swatches (made with 2 
        spider silks) and heady kiolas, which are made with the brewing skill 
        (see <A href="brew.php">the 
        Brewing Guide</A> for info on these). As with studded armor, if 
        you are simply working the skill up I would reccomend just doing the 
        mask for training, as it only requires 1 kiola and 1 swatch. 
        <P>There's only 1 more step after cured silk, which is making Wu's 
        Fighting Gauntlets and the 10-slot backpacks (a very nice moneymaker). 
        Both items triv at 88. To make Wu's, you either need to be a potter or 
        buy poison vials from one (these triv at 148, so finding a potter is 
        preferable). Wu's requires 1 glove pattern, 1 silk swatch, 1 vial of 
        viscous mana (made by enchanter with a poison vial and a pearl), 1 
        greater lightstone and 4 heady kiolas. 
        <P>Backpacks are made with 1 high quality bear skin (fairly rare) and a 
        backpack pattern. 
        <P>Hope this helps.</p>
