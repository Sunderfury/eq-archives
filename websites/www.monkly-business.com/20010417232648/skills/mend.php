<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Skill Guides.</u></b></FONT><p><ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a">
<p><STRONG>Mend</STRONG></FONT><ul><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"></p><p><STRONG>A Primer on the Monkish Art of Healing Thyself in Order to Kill Things and Take Their Stuff Faster</STRONG></p><p>Mend is a Monk-only skill that heals 25% of your total hit points every six minutes of real-life time.  This primer will attempt to instruct you in the use and training of this skill.  Used properly, Mend can reduce your downtime, increase your ability to fight difficult mobs and improve your Monk's overall survivability in the hostile world of Norrath.  Thus letting you kill more things and take their stuff.</p><p><STRONG>The Basics of Mend</STRONG></p><p>When you click Mend one of three things will happen:</p><p><UL><LI>Success: 25% of your maximum hit points (including buffs) will be instantly healed.  This cannot take you over your maximum HP, obviously.
<LI>Failure: Nothing happens, but the skill is considered used for six minutes.
<LI>Critical Failure: The mend backfires and you take 25% of your max HP in damage.  Ouch!  While you cannot be killed directly by a failed Mend, it can reduce your hit points to 1.</UL></p><p>Mend is useful enough to place a hotkey for it on your primary combat hotkey bank.  Mend can be used when feigned without breaking a successful feign death.</p><p>To get the most out of your Mend skill, keep your Bind Wounds skill at maximum out as well.</p><p><STRONG>Improving Mend</STRONG></p><p>You get Mend at level 1.  Go ahead and put one skill point into it to kick it off.  You don't need to, but it helps the skill start to improve.</p><p>Your maximum mend skill for each level is (LEVEL+1) * 5.  You can use skill points to improve it, or it will rise on it's own through practice and use.</p><p>There are three stages that the Mend skill goes through:</p><p><UL><LI>Starting: Until the skill reaches a practical level, it will often fail and you will see as many critical failures as successes.  This is not the time to rely on the skill in the middle of combat.  The best thing to do is to mash the Mend button whenever you are not directly in combat.  Keep pressing the button, and your Mend skill will slowly rise.  After a combat, you can use Mend to reduce your downtime as long as your realize that sometimes you will hurt yourself.  Never use the Mend button when you are under 2 bubbles of health.  You should always be using bandages, anyways, to get yourself up to 50% health before Mending.
<LI>Competency: Around level 8 or so, as long as you have been maxing the Mend skill as you rise in levels, you can start to use Mend in combat and you will rarely see critical failures and will succeed more than you fail.  You should still max the skill as soon as you rise in levels, but you can start to use it in combat.  You should still avoid using the Mend button when you are under 2 bubbles of health.  Many Monks have reported that around level 16-17, you will see more critical fails than you would expect.
<LI>Mastery: Around level 19-20, you will have mastered the skill and no longer will fail or critically fail.  You can rely on the skill to succeed 100% of the time.</UL></p><p><STRONG>Using Mend</STRONG></p><p>There are two different ways of using Mend: Maximize Recycling or Maximize Healing.</p><p>The Maximize Recycling method is to basically use Mend as soon as possible.  As soon as you take 25% damage, press the Mend button and start the six-minute counter until it is ready for use again.  This is a decent solo method (but see Maximize Healing, below, for good counter-arguments).  It's possible to use this in a group, but you should take into account what the healers are doing.  If you are fighting a monster that will not take you below 50%, and you are not expecting to fight for another 4+ minutes, by all means, use Mend as early as suitable in order to recycle faster.</p><p>To Maximize Healing, you should wait to use Mend until after the battle.  Especially when soloing, you can get back to 75% of your health just a minute after battle.  Use bandages (via the Bind Wound skill) to get you to 50% and then Mend.  If you Mend when you are below 50% health, then you are wasting healed hit points that could have been healed by Bind Wounds.</p><p>Mend can also be used as buffered hit points, in order to take a stronger mob than usual.  Effectively, Mend gives the Monk 25% more hit points for the purposes of any one battle.</p><p>I like to do a time stamp when I Mend (type /time), so I will know how long before the recycle.  Some Monks have a timer next to their machines.</p><p>When you are grouped, always let your healers know when:</p><p><UL><LI>You have Mend available for use.  I like to say "fom" (Full of Mend) to let them know and to keep my caster envy to a minimum.
<LI>You are going to use Mend.  "MENDING!" a few seconds before you use it.  Nothing pisses a Cleric off more than him wasting a Heal spell on you with a simultaneous Mend/Cast.
<LI>You are out of Mend and getting low on health.  "Out of Mend!" works as a subtle reminder that you need healing just like that Warrior.</UL></p><p>It's probably worth discussing a few basic rules with the healers in your party before combat begins.</p><p>"I will use my Mend when I get to 2 bubbles of health or as soon as you are OOM."</P></font>

