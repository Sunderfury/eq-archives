<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Quest Guides</u></b><p><ul></FONT><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a">Sejah Ghulam Bracer</FONT></p><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><ul><p>NOTE:  This two-part quest will raise Kerran factions and drop
Erudian factions</p><p>To begin the quest, find Shazda Azad inside Kerra Ridge.  If you
enter the zone and cross the bridge, go straight until you come
to a T intersection.  Go left, and left again inside the tunnel.
 Shazda will be in a room with several other kerrans and
gorillas.  She will tell you to find an erudite emissary and
bring back its head.  I was able to start this quest with an
ìapprehensiveî faction.</p><p>The emissary wanders the northern section of Tox Forest,
generally SE of the Erudian zone and N of the druid circle.  One
location I found the emissary at was +1600, -75 as he roamed. 
The head is not a guaranteed drop; I had to kill 3 emissaries
myself.  They are pushovers at level 42; the eqmaps website
lists this as around a level 20 quest.</p><p>Once you have it, return to Shazda and give it to her.  You will
get experience (barely, heh) and a totem item as payment.  The
totem has 5 charges of <i>Spirit of Cat</i> and weighs 1 stone.  You
donít need to turn this back in later, so if you want to put it
away for later use feel free.  Giving Shazda the emissary head
will also trigger the second quest in the sequence.  She will
name you ìsejahî if you can find and kill Sentinel Creot and
bring back his head.</p><p>Sentinel Creot may wander, I am not sure.  I found him standing
at the dock on the Tox forest edge of the water, almost directly
east of the Kerra Ridge entrance.  I soloed him at level 42, but
being a paladin he put up a good fight with all the heals I
couldnít interrupt.  I got his head the first time, so I am not
sure of the drop percentage.</p><p>Return to Shazda again and hand her Creotís head to be given
your Sejah Ghulam Bracer.  Stats for the bracer are AC 5, Dex+5,
Sta+3, wt 0.2 and is MAGIC  LORE  NO DROP.</p><p>May Quellious guide you, brothers and sisters.</p><p>Shuyun Nakayama</p></ul></ul></FONT>

