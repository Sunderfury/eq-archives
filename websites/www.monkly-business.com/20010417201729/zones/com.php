<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Zone Guides.</u></b></FONT><p><ul><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p align="center">Lenardo's
Guide to City of Mist</p><p align="center">By</p><p align="center">Lenardo
Draconis</p><p align="center">Eye of the
Ages</p><p align="center">The
Nameless</p><p>Click <a href="http://nx.sakura.ne.jp/~chizuya/Rok_maps/cityofmist/Map1_mist.jpg" target="_blank">here</a> to load a Map from Questor of
Norrath's Site, Accurate for the first floor-shows the tunnels
b-e are the tunnel entrances/exits:</p><p>City of Mist is a 35-60
zone located in Emerald Jungle,</p><p>most groups that do this
zone pull to the zone line. however there are many camping spots
in the zone where exp is very good. this zone can comfortably
have 5 in it with no crowding issues. one at temple, one at
stables, one at zone, one at arena courtyards, and one at
castle/reaver streets</p><p>first off the spawn are on
about 13 minute timer or so(never timed it-i should) there are 2
levels to City of Mist, with the first level being the only spot
99% of the adventuring takes place on. this guide does not go
over the 2nd level at all</p><p>first off ALL the spawn on
the ground level are Static, which makes pulling them a breeze
compared to other zones.</p><p>the zone is an east west
zone, meaning you go west to go deeper into the zone,</p><p>there are &quot;ghost
type&quot; spawn- apparitions, spectral couriers, phantoms</p><p>skeleton type spawn-
plaguebone skeletons of varying level/types/p><p>golems- fog, mist, etc</p><p>and Goo</p><p>and a semi rare spawn,,the
captain of the guard-i Believe the captain has the Key to the
castle-never checked it out.</p><p>if you follow these tips
99% of the time you will be fine in the zone.</p><p><em><strong>Tip 1: IF you
see a spawn that &quot;falls&quot; from the air wandering past
you do NOT attack it-its not there, it will ignore you if you
ignore it- there are a few spots on the skyramps that have no
floor in sections, that have wanderers that walk right over the
no floor section, thus creating the &quot;ghost&quot; affect.
don't touch them.</strong></em></p><p><em><strong>Tip 2: DO NOT
sit or stand NEAR the exterior zone walls UNLESS you are by the
zone exit- EVER</strong></em></p><p>there are corridors in the
walls and you can and will agg them thru the walls,, BIG
TRAIN/DEATH don't do it.</p><p><b>Entrance area</b></p><p>The entrance is an east
west tunnel, that branches around a square column, proceeds
around the column and merges again to a bridge with a gate at the
end.</p><p>just around the opposite
side of the column from the entrance are 2 static spawn-either
skel/golem/ghost*. easy straight forward pulls.</p><p>if you go in the water and
swim to the SW corner there is a tunnel, the tunnel goes to 3
spots- Temple Area, Pool Area, and Arena.</p><p>going across the bridge
and opening gate are 3 spawn-static- in front of you- again
skel/golem/ghosts* again straightforward fd pulls </p><p><strong><i>NOTE:</i></strong></p><p><i>* The captain can spawn
at any spot in the front half of the zone.</i></p><p>from here the path turns
north and south.</p><p>north goes to an
intersection where you can go north to the temple area, west to
the pool area or east to a balcony.</p><p>South takes you to an
intersection where you can go south to the stables, west to the
pool area or east to a balcony.</p><p>Balcony North, here are 2
spawn here, any of the golem/skel/ghost* mentioned before, simple
fd splits IF you agg both and don't want to bring them to your
group.</p><p>West will be covered
later-Pool Area</p><p>North takes you to the
Temple area.</p><p><b>Temple</b></p><p>you can get to the temple
2 ways, going over bridge, or swimming under water in the stream.
the stream is along the east end of the temple area.</p><p>this area is a temple,
with 2 sets of stairs goingup, 2 doors in alcoves by stairs. you
can setup camp at the east end of the area, by the stream. <strong><u>DO
NOT GO NEAR THE WALLS</u></strong>-there are corridors on the
north and south side of this area,, DON&quot;T go near.</p><p>up the stairs on the left
are 2 spawn, static.</p><p>up the stairs on the right
are 2 spawn, static. i recommend pulling these LAST and going
thru the lower doors.</p><p>Lower Area: both doors
lead to the same area, with 3 static spawn setup evenly spaced
between the doors, fairly simple pulls, usually chain pulled to
group, normal spawn group skel/golem/ghost* a door is opposite
you if you go in the left door, not sure what it leads to yet,
there is a set of stairs down that to the bottom floor, there are
3 or 4 spawn down on the bottom,,again simple static fd style
pulls. once all these spawn dead i usually do the upper temple
area (up outside stairs)-</p><p><strong>Balcony South</strong></p><p>exactly like balcony
north,,2 spawn</p><p><b>Stable area</b></p><p>there are several little
rooms in this area with about 7 or 8 spawn of the normal 3*types,
all static-normal fd pulling applies.</p><p><b>Pool</b></p><p>you can enter the pool
area from 3 spots from the pool by swimming/hopping up the
waterfall-usually not done.</p><p>or from the east north or
east south.</p><p>the pool area has about 8
static spawns of the normal 3* types - simple fd pulling to
separate. usually pulled by zone group.</p><p>there is one exit weswhich goes around the Arena north and south.</p><p>to get past pool area
without agging anything,, either swim to arena room invis, OR
climb waterfall, go to the center of the pool area at the rear,
cast lev and run DUE west, you should not agg any spawn.</p><p><b>Arena South</b></p><p>there are no spawn in this
courtyard, there is a door to the north which leads into the
arena building, there is a building to the west, and an exit that
merges on the west side of arena building from Arena North. again
DO NOT GO NEAR THE EXTERIOR WALLS and if you see a wanderer,,DO
NOT TOUCH IT-it is not there.</p><p>building on the west has 2
entrances the upper level is empty. a set of stairs lead down and
there are 3 Goo's (level 38-40ish) here, normal pulling applies.
the path thru the southmost door, so pull thru that door.</p><p>Arena Building, this room
has 3 Goo's in it, all static, with one being on the center
island.</p><p><i>Note: Goo's do NOT see
invis so you can get directly to this area by getting invis cast
upon yourself and swimming here, then hopping onto the center
island and jumping across the moat.</i></p><p><i>Short races have
problems getting on center island</i></p><p>the one on center island
is around 45th level. the center island is flagged ARENA--PK is
allowed.</p><p>center island goo usually
pulled to arena north due to pathing. </p><p><b>Arena North</b></p><p>this courtyard is devoid
of spawn. like arena south it may have a wandering spawn that
&quot;isn't there&quot; do not touch them and again DO NOT GO
NEAR THE EXTERIOR WALLS.</p><p>to the south is the other
door to the arena, the goo in here usually path thru the north
door so most people pull from here. to the west is a building
with 2 entrances.</p><p>west building has 4 or 5
of the 3 types of spawn and path out thru the south most door,
again normal pulling techniques apply.</p><p>Moving West the path again
splits around a &quot;church&quot; which has 2 spawn in it.
normal pulling -usually pulled by south arena group -pathing
takes it thru the building on the south side.</p><p>going north and south open
to courtyards. both courtyards are devoid of spawn,</p><p>Do not go near the
buildings or the exterior walls.</p><p>there are exits west from
both courtyards, Reaver street north and reaver street south.</p><p><strong>Reaver Street
South</strong></p><p>path leads west then does
a 180 north the goes east to the Gazebo and enters the castle
area. there are 4 spawn at the 180 usually one of the normal 3
types of spawn-i have never seen captain spawn beyond the pool
area.</p><p>for pulling these hug the
inner wall and pull via agg since the pathing in the corridors in
the walls get closer to the ground. </p><p><strong>Reaver Street
North</strong></p><p>exactly like reaver street
south, but mirrored, leads to castle area.</p><p><i>Note: to get to
gazebo's in castle area WITHOUT having to fight the 4 at the 180
switchback, in the courtyard-north side, cast lev. goto rear of
church on outside and go up step, then up as high as you can go
on piller base. then look upwards some and RUN around the corner
thru archway, you can get on the inner wall and drop to other
side of reaver street safely.</i></p><p><b>Castle Area</b></p><p>there are 2 gazebo's with
3 spawn in each gazebo. simple pulls usually, setup for initial
break by the north reaver street, at the set of steps JUST
outside the 180 switchback agg range, you can fd pull if nec or
-get a HIGH level person to pull, usually can get one.</p><p><b>Castle </b></p><p>No Data as of yet. but you
need the key or a rogue with 201 skill in Pick Locks to open the
door. </p><p>Also:</p><p>Black Reavers - 100% magic
resist, when they die they immediately respawn into another one,
then spawn into one of the 3 lords. of which Lord Rak'ishiir is
the toughest one. if lord Rak spawns pull him to a courtyard and
RUN/Die.</p><p>most normal techniques for
fighting him do NOT work. currently the way he is killed is with
MAGE pets,,specicially FIRE ELEMENTALS- Lord Rak casts a NASTY
AoE spell and will wipe out any normal group in seconds, Fire
elementals are IMMUNE to this spell(unless changed recently) and
they are sent in to kill him tanks sit around out of aoe range
while the pets do all the work. the Lure line of wiz spells i
believe are the ONLY spells that will affect him that i know of.</p><p>the closer to the castle
you go the Higher the level of the spawn with the gazebo area by
castle pretty much &quot;greening&quot; out at level 55. there
are higher level spawn INSIDE of the castle but as of this time i
have not been inside there.</p><p>Drops are completely
Random and RARE..so don't go expecting to get anything in
particular-with investing a week or so here-took me a month to
get a trancing stick,,i BOUGHT it.</p></FONT>

