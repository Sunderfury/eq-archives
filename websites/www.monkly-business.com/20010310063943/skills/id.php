<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Skill Guides.</u></b></FONT><p><ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Zadkiels guide to Instill Doubt for Monks.</b></FONT></p><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a">
<p>Contents</p><A HREF="#1">1) What is it?
</A><br><A HREF="#2">2) When do I get it?
</A><br><A HREF="#3">3) How do I raise it?
</A><br><A HREF="#4">4) How do I use it?
</A><br><A HREF="#5">5) When do I use it?
</A>
<p>I was asked to write this guide by the posters at the <A HREF="http://pub18.ezboard.com/bmmrpgdiscussion">Everquest Monks Forum</A> in order that all the information that has been gathered about it could be put into 1 single document. This is version 1.0 of the document, dated Feb 23rd, 2000.</P>
<P><A NAME="1"><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>1) What is it?</b></FONT></A><P>Instill Doubt is a skill usable by all 3 classes with access to rogue skills (Monk, Rogue and bard). It was accidently missed off the monk trainers when EQ underwent commercial release, and that fact was not discovered for many months, until one monk received the skill by accident when a GM used a special command to restore his character. This monk questioned the fact that he suddenly got the skill, and Verant finally admitted that all monks should have received it, and added it to our trainers</P><P>When the skill is used, you attempt to intimdate your target into running away by performing a Martial-Arts 'kata' to demonstrate your power to them</P>
<A NAME="2"><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>2) When do I get it?</b></FONT></A>
<P>Once you reach level 18, you can learn the skill by training at any of the Monk trainers in Freeport, Qeynos, Rathe Mtns or South Karana.</P>
<A NAME="3"><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>3) How do I raise it?</b></FONT></A>
</P>Instill Doubt is the hardest of all monk skills to raise. When you put your first training point into the skill at level 18, that will grant you 18 skill in the ability. It is generally accepted that the skill goes up much slower between 18-40, and that increasing the skill to 40 using training points will save many weeks of frustration trying to build up the skill manually. having a total of 23 skill points available at level 18 will allow you to buy the skill up to 40.</P><P><UL><LI>The skill can be raised by practising it while having <EM>anything</EM>targetted (except yourself). NPCs, PCs, Pets, Corpses, Boats... if you can target it, you can raise ID on it.<LI>The skill will only raise on a successful skill check.<LI>You do not need to be in range of your target for skill to get an increase. You will get a message that the target is out of range, but there is still a chance of a skillgain.<LI><i>Note: Instill Doubt can be practiced at any time: while standing, while sitting, or while feigned.  Also note that it will break feign when it "goes off" whether or not it was successful.</i></UL><P>So, practice, practice, practice. When making those long runs across the Karana's, target a guard and ID as you run. When travelling across the sea, practice on the boat. When waiting between pulls at a camp, practice on your groupmates. Mash that ID button at each and every opportunity, and it will slowly, slowly creep upwards.</P>
<A NAME="4"><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>4) How do I use it?</b></FONT>
</A><P>The secret to successful use of ID is knowing that it counts as a special attack, even though it does not grey out, nor is it greyed out by, a special attack.</P><P>If your special attack button is greyed out when the ID 'kicks' at the end of its animation, the result is an automatic failure of the skill. Also, if your special is available, and then your ID succeeds, your special attack will not work for a period of time equal to one kick. If you activate the special in this time it will fail to work.</P><P>A summary of the requirements for a successful ID:<OL TYPE=a><LI>Your 'special attack' button is not greyed out when ID 'kicks'.<LI>The skillcheck for ID succeeds.<LI>A further skillcheck on ID is done to attack the target. This is done as a 'kick' using the skill of ID rather than your kick skill. This kick must connect.<LI>If the target can only be hurt by magical means, you must be wearing magical boots.<LI>The target must not resist the fear spell.</OL><P>Thats a lot of 'ifs' isnt it? Well don't give up on the skill just yet.  "a" can be dealt with using a little trick I will detail below. "b" and "c" will improve as the skill improves. "d" can be forgotten about after a small investment in footwear, but unfortunately there is little you can do about "e", except to have magic-resist debuffs cast at the target.</P><P>Timing ID and your specials:</P><OL TYPE=1><LI>With both buttons available, press the 'Instill Doubt' button.<LI>When ID 'kicks' and you get the message indicating it succeeding or failing, immediately press the button for your special attack.<LI>When ID pops back up, hit it immediately.<LI>When your special pops back up, <EM>do not</EM>press it immediately, instead wait for ID to 'kick', then press it.<LI>Repeat from 3.</OL><P>This guarantees that ID gets a chance to succeed every time.</P><P>If you successfully fear your target, run after it, whacking it in the back, doing free damage while it is unable to hurt you in return. Keep using ID with the above timing, because if you suceed on another ID while it is still running from the first, it will extend the fear spell.</P>
<A NAME="5"><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>5) When do I use it?</b></FONT></A><P>Because ID causes your target to run away from you, there are many circumstances when it should not be used. A common nickname for Instill Doubt is 'Instill Train', because when used in a dungeon, the results are almost invariably lethal for the monk and his group, as the feared creature runs for his life, agrovating every other creature it runs near to attack the monk, causing horrific trains.</P><P>For this reason ID should only be used in outdoor zones, and even then, when you know that the pathing will not cause the target to run near allies.</P><BR>
