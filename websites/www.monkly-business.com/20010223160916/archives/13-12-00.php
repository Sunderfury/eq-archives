<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Life Sucks, and then you die.</b></FONT><br>
<FONT size=2 face="Arial, Helvetica, sans-serif">Submitted by <a href="mailto:zadkiel@monkly-business.com">Zadkiel</a></FONT></p>
<ul>
<FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p>Yesterday was quite possibly the worst day of my life.</p><p>It started innocently enough, with an average day at work, followed by an entertaining evening with some friends, but the story starts at about 10.30pm when I headed back to where my motorbike was parked (I ride a 600cc Bandit) to drive home.</p><p>Some car driver had hit my bike (parked in a bike bay on the side of the road) and then just driven off. About $2000 of damage. I did the standard things and went to the police to report the incident, so by the time I got home (by taxi) it was 00.30am.</p><p>I opened the door to my house to discover the place turned over. My PC that I use to play EQ on stolen, along with other electical appliances (TV, Video etc, etc) and the place looking like a scene from World War III.</p><p>So I called the Police out, and by the time they had arrived, looked the place over, taken a statement, filled in all the paperwork etc etc it was 4.00am.</p><p>I went to bed. Let me tell you, it is a VERY freaky experience trying to sleep in  a house that has just been broken into.</p><p>So, I get up for work at 7.00am, and realise I dont have a bike to ride into work on, and no cash to get a taxi, so I get dressed and head to the Bank. I put my card in the cash machine, which promptly swallows it, gives some lame excuse about a technical fault, and then sits there smugly staring at me.</p><p>I am a strong believer in Karma, and someone somewhere owes me BIG for last night. I think I'll go buy a lottery ticket....</p></FONT></ul>

