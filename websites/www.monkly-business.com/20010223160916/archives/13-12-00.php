<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Patch Day!</b></FONT><br>
<FONT size=2 face="Arial, Helvetica, sans-serif">Submitted by <a href="mailto:zadkiel@monkly-business.com">Zadkiel</a></FONT></p>
<ul>
<FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p>Patch day comes around again. This one looks good for us monks!</p><p>A few snippets from the patch message:</p><p><b>Corrected an older bug that would cause people to stop gaining or losing faction with associates when the primary faction was at its maximum or minimum. Players will now be able to further modify associate factions in the appropriate direction.</b></p><p>This one affected everyone, but its good news for us monks, as this bug made building some factions very difficult. Now Human monks will be accepted in Cabilis more easily, and Iksar monks will have a MUCH easier time building many factions, including those with humans</p><p><b>- The Monk's hand-to-hand skill cap has been raised to a level equal to their one-handed blunt skill.</b></p>YES! Finally. It only took 6 months of complaining, but with the advent of the new Celestial Fists encouraging us to use Hand to Hand skill, it could no longer be ignored. WTG Verant for finally listening.</p><p>Anyway, servers will be down for a while, why not pop into the <a href="/chat/index.html">chat room</a> to discuss these changes while we wait?</p></FONT></ul>

