<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Patch Message</b></FONT><br>
<FONT size=2 face="Arial, Helvetica, sans-serif">Submitted by <a href="mailto:zadkiel@monkly-business.com">Zadkiel</a></FONT></p>
<ul>
<FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p>Heres the patch message for today! Enjoy!</p><p><ul><i>September 28th, 7:00am </p><p>------------------------------ </p><p>***Patch Day*** </p><p>The following changes have been implemented on the production
servers as part of today's patch: </p><p>*General Changes* </p><p>- NPC corpses level 55 or over will now last 30 minutes before
decaying. This should help those on large multi-group
encounters who need some prep-time before looting items. - The
item "Woe" has been repaired. If you receive the "Item out of
charges" message when your "Woe" attempts to fire its spell,
please contact a GM to trade out the item. - The spell "Mark of
Karn" has been changed. The recast-time has been reduced
significantly and the Hitpoint-return is now scaled inversely
based upon the delay of the weapon. In addition, a hitpoint
return will only take place when the target is hit with a primary
melee weapon, or the primary hand if you are unarmed. - Some
more tradeskill component vendors have been placed within the
world to make certain tradeskills easier to use in your home
town. - "AFK" and "LD" status will now be reported above
player's heads. </p><p>*Fleeing NPCs* </p><p>Last week's patch caused NPCs to flee more than before due to
a bug that was fixed. After evaluating the situation, we decided
to reduce the chance to flee, but not to the extent that it was
reduced before. NPCs were previously much more "brave" than
they should have been. </p><p>As part of the same change, charmed NPCs also began fleeing
when low on health. We've determined that this is not a good
situation for the players. As such, we have stopped charmed
creatures from fleeing per player request. </p><p>*Level 1 Character Purge* </p><p>The Level 1 character purge has been completed. All level 1
characters that have not been played within the past 30 days
have been deleted. </p><p>*New Server Active* </p><p>Tholuxe Paells, a new server open to new characters only, is
now active and ready for play. Enjoy! As a reminder, /movelog
registration will take place on Mithaniel Marr and Tunare Friday
evening from 6PM to Midnight (PDT: GMT-7). Registered
characters will be moved to a second new server, Drinal, early
next week. </p><p>***User Interface Changes*** </p><p>Many UI enhancements were made in today's patch: </p><p>*Moving and Resizing Windows* </p><p>The data windows in the full screen "HUD" mode may now be
moved by clicking in the upper left-hand corner, moving them to
where you want, then clicking again. The "spell effect" and
"chat" windows can be resized by clicking on the lower
right-hand corner, dragging to the appropriate size, and then
clicking again. Coordinates for window locations in each
resolution are stored in the EQCLIENT.INI file. </p><p> *Bugs with Several Commands Fixed* </p><p>We fixed a problem with /book, /sit, and /doability that allowed
them to be executed at inappropriate times, such as while
charmed or feared. They should now only function when the
native button would have functioned if pressed. </p><p>*Miscellaneous Additions* </p><p>Added the following /-commands: </p><p>- /camp: Causes your character to camp out. You must be sitting
to use /camp. - /disband: Will disband you from your current
group, or turn down an invitation to join another group. - /invite:
Will invite the target into a group, or accept an invitation if one is
pending. - /attack: Will toggle the state of "Auto-attack". You
may also add a parameter of "on" or "off" to explicitly set the
state. </p><p>In addition: </p><p>- CTRL-1 through CTRL-8 will cast the spell in the specified
spell-slot. - ":" (colon: Short for /emote) and "'" (apostrophe:
Short for /say) will now bring up the chat line in the same fashion
as the /-key and ENTER. - The "R" key (by default) takes you into
reply-mode as always, bringing up a reply to the last person
sending you a /tell. However, you can now TAB and SHIFT-TAB to
cycle through the last 10 people that have sent you /tells. </p><p>*Pauses in Socials*</p><p>You may now enter pauses in social batches with the /pause
command. /pause takes an argument from 1 to 600, with the
value being equal to the number of tenths of seconds (i.e.
/pause 100 will yield a 10 second pause). </p><p>For example, a social consisting of: </p><p>/sit off /cast 1 /pause 100 /sit on </p><p>...would cause you to stand up, cast the spell in slot 1, wait 10
seconds after you've STARTED casting the spell, and sit down. </p><p>Furthermore, you can also use the /pause command on the same
line as another social by separating the commands with a
comma. Even though the pause command is specified first, it is
executed AFTER the command on the same line. </p><p>For example:</p><p>/sit off /pause 100,/cast 1 /sit on </p><p>...would have the same effect as the example above. It is
important to note that the comma can only be used to separate
a /pause command from another, and MUST be specified first on
the line. </p><p> *Emote Codes* </p><p>The @ symbol has been replaced with a series of %-codes that
expand into various pieces of information, and now works in ALL
chat channels (emote, say, shout, ooc, auction, group, guild). </p><p>The new %-codes are: </p><p>%T - Returns the current target. %S - Returns the subjective
gender-specific pronoun for the target (He, She, It). %O -
Returns the objective gender-specific pronoun for the target
(Him, Her, It). %P - Returns the posessive gender-specific
pronoun for the target (His, Her, Its). %R - Returns the race of
the target (only works on Player characters). </p><p>For example, if you have a target of "a gnoll", the following
emote: </p><p>/point say points at %T indicating that he is ready to kill %O. </p><p>...Your character will execute the "point" animation with a text
output of "CharacterName points at a gnoll indicating that he is
ready to kill it." </p><p> - The EverQuest Team</i></ul></p></FONT></ul>

