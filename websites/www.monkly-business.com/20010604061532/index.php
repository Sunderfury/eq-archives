<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Its ALIVE!</b></FONT><br>
<FONT size=2 face="Arial, Helvetica, sans-serif">Submitted by <a href="mailto:zadkiel@monkly-business.com">Zadkiel</a></FONT></p>
<ul>
<FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p>OK, I'm sorry, and you can stop sending the 'are you alive' emails now. I went through a bad patch at work, and the last thing you want to do after spending a nice 16 hour day coding and scripting, is go home, and do some more coding and scripting, so the site was neglected for a while, and I am sorry.</p><p>However, I am putting up this article because I have a problem. My problem is the $300 bill I have to pay for the excess bandwidth I used in the last 2 months. I can't figure it out, I stopped updating the site, and my bandwidth usage, which had been under 2gb/month, leapt up to 6gb in march, and then 8gb in April. I've already gone well over my allowance for May, and its only the 12th, I'm going to get another bill at the end of the month for that too.</p><p>So, Ive done some work on the site today, to try and reduce bandwidth. The server status will now only update once every 10 minutes, not every time you load a new page, this change was necessary to help with the problem. Also all the large files on the site (namely the excel files and the pictures in the humour section) have been moved off the server</p><p>These changes will hopefully mean a large reduction in the bandwidth I use, saving me some money, but I have another problem.</p><p>I recently learned asp and sql programming, and have some wonderful ideas for the site that you would all love. A team of reporters able to submit articles, meaning multiple news articles per day. Searchable databases of monk equipment, and more. Sounds good eh?</p><p>But wait, I said I had a problem, and the problem is this: My site is hosted on a unix box with php support, not asp, and I dont know any php.</p><p>Hopefully, with someone's help, I can solve both problems in 1 go. I need a server, a nice fast server supporting asp and sql. I'm hoping one of you monks out there is the server admin at an ISP, or something like that, and can host my site for a reduced price, or perhaps even for free?</p><p>If things keep going the way they are right now, I will not be able to afford to keep the site going for much longer. I am not going to add ad-banners, or pop-ups, or ask for donations, or make the site pay-per-view. I would rather take the site down than do something like that, I just need a nice cheap hosting solution, and I will swallow my costs so far.</p></FONT></ul>



