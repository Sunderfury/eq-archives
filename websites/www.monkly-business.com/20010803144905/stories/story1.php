<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Stories.</u></b></FONT><p><ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a">Qilamil's Task</p></FONT><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><p>Qilamil woke this morning just like every other morning, except today was not 
like every other morning.  For today was the day where his 13th Birthday, and 
the day he would be able to begin his journey as a Wizard, Enchanter, 
Magician, Warrior, Paladin, Cleric, Monk, Bard, Druid, and Ranger, Rogue, or 
maybe explore the darker side and be a Necromancer or Shadow Knight. He has 
been raised as an agnostic, and he chose that he will become a monk to serve 
the Silent Fist Clan and protect his hometown of Qeynos.</p><p>No sooner had he tried to think of where the guild was, did a note 
appear in his hand. "Come to the Silent Fist Clan to receive your training, 
we are located in the building that overlooks the Reflecting Pond on the 
North Side of Qeynos. Seek the guild master Lu'Sun and hand this note to him 
as proof of your membership."</p><p>So he put on his Raw Silk Robe his mother had sewn for him, and grabbed his 
two Enameled Black Maces his grandfather had given him, strapped his White 
Sash his sister had given him (Dagne, his sister, was a monk who went to 
Freeport to study) and set off.</p><p>As he walked through the city he had never noticed how beautiful it really 
was. He opened the door of the Silent Fist Clan and looked inside. There was 
a bunch of other higher-ranking monks there waiting to see the initiation of 
the new members. I found a small building behind the real clan and Lu'Sun was 
in there, and he smiled as I walked in.</p><p>I handed in my note and he gave me a new training tunic and told me to wear 
it instead of my robe until I prove myself worthy of a robe. He told me to go 
see Phin Esrinap for training. I went searching and I found him standing and 
talking to a fellow monk near the Priest of Discord. I told him Lu'Sun had 
sent me, and he told me about the Headbands.</p><p>He told me that to earn the first headband, the White Headband, I had to 
bring in the scalps of four Gnoll Pups that usually wander outside the city 
gates.</p><p>I walked and took my first steps beyond the city gates, and saw bats, rats, 
snakes, and I got a glimpse at a gnoll, which was the first time I ever saw 
one of those gruesome creatures. He charged at me, yelling things in 
Gnollish, and he slashed me with his sword. He was a tough fight, but was 
defeated and gave up a scalp.</p><p>I continued to hunt Gnoll Pups, and it had become rather easy when some High 
Ranking druid had cast a spell on me, and I felt strengthened. Suddenly, 
there was a cry for help to the left of me.</p><p>I run over and I see a fellow monk fighting a full-grown gnoll, named Fippy 
Darkpaw. I started crushing it with my mace and soon we had both killed 
Fippy. The young monk I had saved, Tomer Instogle, explained he is lost and 
needs help reporting back to his master, Seta Bakindo, back at the Silent 
Fist Clan.</p><p>When we got back, Seta offered her gratitude to me and I had earned her trust 
with important matters. Tomer then gave me a gnoll pup scalp, the 4th one I 
needed.</p><p>I ran over to Phin Esrinap, again near the Priest of Discord, and handed him 
the gnoll pup scalps. "Very good Qilamil, you have proved you are worthy of 
the White Headband."</p><p>I put the white headband on with pride and felt magically strengthened. So 
far I am of the 2nd Rank, and have brought pride and honor to the Silent Fist 
Clan.</p>
