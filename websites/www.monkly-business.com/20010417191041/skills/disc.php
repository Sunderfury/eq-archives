p><p>100 FIST  (Level 57): When using this discipline, the monk will be hasted (e.g. will hit much faster).  This haste stacks with every other haste in the game, including haste items and spells.  This discipline lasts for 15 seconds, and has a base reuse time of 30 minutes.  The reuse time for this discipline will decrease as the monk gains additional levels.</p><p>SILENTFIST (Level 59): When this discipline is activated, the next successful Dragon Punch (Tail Rake for Iksar) will do additional damage, and have a chance of stunning the target.  The base reuse time for this discipline is 9 minutes, and will decrease as the monk gains additional levels.</p><p>ASHENHAND (Level 60): When this discipline is activated, the next Eagle Strike will do a surprising amount of damage, and under certain circumstances may have a chance to kill the target outright.  The base reuse time for this discipline is 72 minutes.</P></font>

