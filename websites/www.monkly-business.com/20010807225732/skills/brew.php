        <P>This is, however, more information than you need. As we (monks) do 
        not have foraging, it really is much easier if you have the cash to just 
        do the store-bought recipes. What I would suggest is: 
        <P>
        <OL>
          <LI>Put in 20 skills points, as noted above; 
          <LI>Brew short beers to 31; 
          <LI>Brew mead to 41; 
          <LI>Brew short ale to 51; 
          <LI>Brew ale to 68; 
          <LI>Brew Ol' Tumjum's the rest of the way (trivs at 135). </LI></OL>
        <P>A few last notes: 
        <P>
        <UL>
          <LI>In recipes requiring multiple instances of the same item, do NOT 
          stack them in the barrel. 
          <LI>Use store-bought water, foraged water doesn't work. 
          <LI>Use <CTRL><LEFT-CLICK>to select 1 item from a stack. 
          <LI>If you can afford it, pound your product instead of selling it. 
          Not really sure what the advantage is to getting alcohol tolerance to 
          master, but it's easy to do while brewing so why not? Keep in mind 
          though that drinking too much can lower your INT. </LI></UL>
        <P>Hope this helps.</p>
