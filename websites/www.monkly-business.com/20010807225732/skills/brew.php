<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Skill Guides.</u></b></FONT><p><ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><CENTER><STRONG>Sensei's Brewing Guide</STRONG></CENTER></FONT></p><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a">
<P>If you are one of those do-it-yourself monks, then eventually you 
        will want some cured silk armor. Making cured silk requires Heady 
        Kiola's. While you can certainly just hit up a brewer for one, brewing 
        is rather easy skill to work up to proficiency and in the long run it's 
        much easier to just do yourself. 
        <P>This is my brewing guide, compiled from a number of other sources. No 
        roleplaying, no dancing around, just the facts, ma'm. This guide assumes 
        you know what a brewing barrel looks like and are smart enough to use 
        the 'combine' button in said barrel. Brewing barrels can be found in 
        every city I can think of, but for pure convenience I prefer working 
        this skill in Freeport (tons of barrels, multiple merchants with the 
        items you need, etc.). 
        <P>To start off, put the obligatory 20 training points into the skill at 
        your guild. While you can skip this step (put 1 in at least though) it 
        will save you that much time and money down the road. 
        <P>The most important thing to know in brewing (or any trade skill) is 
        the order of triviality. For those of you new to trade skills, the 
        "trivial" point is the skill point at which you will no longer get skill 
        increases trying to make an item. Keep in mind that you can still fail 
        something trivial, so it is usually wise to go beyond the skill you are 
        aiming for; i.e., Heady Kiola's "triv" at 46, but you would be better 
        off going to say 60ish if you want to reduce the hassle of failures. 
