<p><FONT size=5 face="Arial, Helvetica, sans-serif" color="#334455"><b><u>Quest Guides</u></b></FONT></p>

<ul><FONT size=5 face="Arial, Helvetica, sans-serif" color="#8b1a1a"><b>Guide to the Collar of Neshika Quest</b></FONT>

<ul><FONT size=3 face="Arial, Helvetica, sans-serif" color="#8b1a1a">


<p>The Collar of Neshika is not the best neck item out there, and furthermore it weighs 1.5 stone so almost everyone prefers the Collar of Undead Protection. But some monks love the +9 stamina. Anyway, the Collar of Neshika is a monk-only item and Grim Oakfist needs our help.</p>

<p>Furthermore, I believe that this quest could be completed by someone at level 18ish (not 25 as is often listed) and maybe lower if they had a group, and at that level the Collar is quite a good item.  Of course, you must stay out of Kithicor at night or the undead will get you... this is a daytime-only quest.</p>

<p>The quest starts with Grim Oakfist in Kithicor forest. He spawns in one of the houses right by the West Commons zone line - the one to the south of the path (the left as you walk in). He has two placeholders, Brollus Hoost (level 18ish with tradefolk and corrupt guard faction) and Tallyn Starwatch (level 30 with ranger faction). This is the frustrating part of the quest, you have to wait for somebody (or the undead?) to kill the placeholders until Grim shows up.</p>

<p>If you hail him, you will get the quest pretty easily. You ask him "What mission in the woods?" and then "I am a fellow monk." and he asks you to kill the three tigers who killed his family and return their pelts. These three tigers spawn in Kithicor when you say the "I am a fellow monk" line. All of these tigers were fully green to me at level 27, which I think means that they must be at most level 16.</p>

<p>You should first go after Kobb because he runs around. Fast. I think you need SOW to catch him. I don't know where he spawns, but I have seen him twice on the path not too far west of the West Commons zone. He seems to run back and forth there. When you kill him he drops "a strange tiger pelt".</p>

<p>The other two are easier to find - I think that they don't move. Reggit spawns at -850,1000 and Eenot spawns at -800,4150, i.e. both along the south wall. Reggit drops "a peculiar tiger pelt", and Eenot drops "an unusual tiger pelt".  Note that Reggit is Tigger spelled backwards, and Eenot is a lot like Tony spelled backwards, har!</p>

<p>Killing the tigers gives you bad ranger faction: worse with KithicorProtectors, ProtectorsofTine, JaggedPineTreefolk; better with UnkemptDruids. So if you have a ranger helping you track, she might want to avoid the fight.</p>

<p>Once you have all the pelts, give em to Grim for the some quite nice experience (about a third of a bubble for me at level 29), faction increase for AshenOrder SilentFistClan and KnightsofTruth, and your reward:</p>

<p><img src="collarofneshika.jpg"></p>

<p>Happy hunting!</p>

